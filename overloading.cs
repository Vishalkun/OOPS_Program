using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace OOPS_Concept
{
    internal class overloading
    {
        
        

            
            public int Add(int a, int b)
            {
                int sum = a + b;
                return sum;
            }

            
            public int Add(int a, int b, int c)
            {
                int sum = a + b + c;
                return sum;
            }

            
               

              
            
        
    }
